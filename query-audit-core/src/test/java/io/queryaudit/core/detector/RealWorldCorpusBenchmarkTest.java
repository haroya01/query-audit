  @Nested
  class PetClinicCorpus {

    private final IndexMetadata schema = createPetClinicSchema();

    /**
     * All PetClinic queries are well-written with explicit columns, proper WHERE clauses, and
     * indexed lookups. They should produce minimal detections.
     */
    @Test
    void analyzeAllQueries() {
      List<QueryRecord> queries =
          List.of(
              // Q1: Owner search by last name (indexed, LIKE with trailing wildcard)
              record(
                  "SELECT id, first_name, last_name, address, city, telephone FROM owners WHERE last_name LIKE ?"),
              // Q2: List all types (small reference table)
              record("SELECT id, name FROM types ORDER BY name"),
              // Q3: Find pets by owner (indexed FK)
              record(
                  "SELECT pets.id, pets.name, pets.birth_date, pets.type_id, pets.owner_id FROM pets WHERE pets.owner_id = ?"),
              // Q4: Find visits by pet (indexed FK)
              record(
                  "SELECT visits.id, visits.visit_date, visits.description, visits.pet_id FROM visits WHERE visits.pet_id = ?"),
              // Q5: Insert new owner
              record(
                  "INSERT INTO owners (first_name, last_name, address, city, telephone) VALUES (?, ?, ?, ?, ?)"),
              // Q6: Update owner
              record(
                  "UPDATE owners SET first_name=?, last_name=?, address=?, city=?, telephone=? WHERE id=?"),
              // Q7: Find owner by ID (PK lookup)
              record(
                  "SELECT id, first_name, last_name, address, city, telephone FROM owners WHERE id = ?"),
              // Q8: Insert new pet
              record("INSERT INTO pets (name, birth_date, type_id, owner_id) VALUES (?, ?, ?, ?)"),
              // Q9: Update pet
              record("UPDATE pets SET name=?, birth_date=?, type_id=?, owner_id=? WHERE id=?"),
              // Q10: Insert new visit
              record("INSERT INTO visits (visit_date, description, pet_id) VALUES (?, ?, ?)"),
              // Q11: Find vet by last name
              record("SELECT id, first_name, last_name FROM vets WHERE last_name = ?"),
              // Q12: Find vet specialties (JOIN)
              record(
                  "SELECT v.id, v.first_name, v.last_name, s.name FROM vets v JOIN vet_specialties vs ON v.id = vs.vet_id JOIN specialties s ON vs.specialty_id = s.id"),
              // Q13: Find pet by ID
              record("SELECT id, name, birth_date, type_id, owner_id FROM pets WHERE id = ?"),
              // Q14: Count owners
              record("SELECT COUNT(*) FROM owners"),
              // Q15: Find pet type by ID
              record("SELECT id, name FROM types WHERE id = ?"));

      QueryAuditReport report = analyzer.analyze("PetClinicCorpus", queries, schema);

      // --- True positive expectations ---
      // These are INFO-level detections that are legitimate observations:
      // - UNBOUNDED_RESULT_SET on Q2 (SELECT without LIMIT on types) and Q12 (vet specialties join)
      //   are acceptable INFO warnings.

      // --- False positive checks ---
      // PetClinic queries are well-indexed. There should be no ERROR-level issues.
      assertThat(report.getErrors())
          .as("PetClinic queries should not trigger ERROR-level issues")
          .isEmpty();

      // No SELECT * usage
      assertThat(
              report.getConfirmedIssues().stream()
                  .filter(i -> i.type() == IssueType.SELECT_ALL)
                  .toList())
          .as("PetClinic does not use SELECT *")
          .isEmpty();

      // No missing WHERE index (all WHERE columns are indexed)
      assertThat(
              report.getConfirmedIssues().stream()
                  .filter(i -> i.type() == IssueType.MISSING_WHERE_INDEX)
                  .toList())
          .as("All PetClinic WHERE columns have indexes")
          .isEmpty();

      // No cartesian joins
      assertThat(
              report.getConfirmedIssues().stream()
                  .filter(i -> i.type() == IssueType.CARTESIAN_JOIN)
                  .toList())
          .isEmpty();

      // Precision evaluation
      PrecisionResult result =
          evaluatePrecision(
              "Spring PetClinic",
              report,
              // Expected: UNBOUNDED_RESULT_SET on reference table queries is acceptable
              Set.of(IssueType.UNBOUNDED_RESULT_SET, IssueType.COVERING_INDEX_OPPORTUNITY),
              Set.of());

      System.out.println("PetClinic total queries: " + queries.size());
    }

    @Test
    void noFalsePositiveOnIndexedLookups() {
      // Individual PK lookups should produce zero confirmed issues
      List<QueryRecord> queries =
          List.of(
              record("SELECT id, first_name, last_name FROM owners WHERE id = ?"),
              record("SELECT id, name FROM types WHERE id = ?"),
              record("SELECT id, name, birth_date FROM pets WHERE id = ?"));

      QueryAuditReport report = analyzer.analyze("PetClinic_PKLookups", queries, schema);

      assertThat(report.getConfirmedIssues())
          .as("PK lookups should not trigger any confirmed issues")
          .isEmpty();
    }
  }

