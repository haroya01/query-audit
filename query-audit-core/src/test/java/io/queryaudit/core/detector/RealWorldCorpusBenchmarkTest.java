    assertThat(report.getConfirmedIssues())
        .as("Known bad queries should produce confirmed issues")
        .isNotEmpty();

    // Verify known bad patterns are detected
    List<IssueType> confirmedTypes =
        report.getConfirmedIssues().stream().map(Issue::type).distinct().toList();

    List<IssueType> allDetectedTypes = new ArrayList<>(confirmedTypes);
    report.getInfoIssues().stream().map(Issue::type).distinct().forEach(allDetectedTypes::add);

    // SELECT_ALL is now INFO severity
    assertThat(allDetectedTypes)
        .as("SELECT * should be detected in full corpus")
        .contains(IssueType.SELECT_ALL);
    assertThat(confirmedTypes)
        .as("UPDATE without WHERE should be detected in full corpus")
        .contains(IssueType.UPDATE_WITHOUT_WHERE);
