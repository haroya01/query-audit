  /**
   * Categorizes each issue from a report as true positive or false positive based on expected
   * detection types and prints a summary.
   *
   * @param corpusName human-readable name for the corpus
   * @param report the analyzer report
   * @param expectedIssueTypes issue types that are expected (true positives)
   * @param expectedQuerySubstrings SQL substrings where detections are expected
   * @return a PrecisionResult with counts
   */
  private static PrecisionResult evaluatePrecision(
      String corpusName,
      QueryAuditReport report,
      Set<IssueType> expectedIssueTypes,
      Set<String> expectedQuerySubstrings) {

    List<Issue> allIssues = new ArrayList<>();
    allIssues.addAll(report.getConfirmedIssues());
    allIssues.addAll(report.getInfoIssues());

    int truePositives = 0;
    int falsePositives = 0;
    List<String> fpDetails = new ArrayList<>();

    for (Issue issue : allIssues) {
      boolean isExpected = expectedIssueTypes.contains(issue.type());
      boolean matchesQuery =
          expectedQuerySubstrings.stream()
              .anyMatch(
                  sub ->
                      issue.query() != null
                          && issue.query().toLowerCase().contains(sub.toLowerCase()));

      if (isExpected || matchesQuery) {
        truePositives++;
      } else {
        falsePositives++;
        fpDetails.add(
            String.format(
                "  FP: [%s] %s on %s.%s",
                issue.type().getCode(), issue.severity(), issue.table(), issue.column()));
      }
    }

    int total = truePositives + falsePositives;
    double precision = total == 0 ? 1.0 : (double) truePositives / total;

    System.out.println();
    System.out.println("=== " + corpusName + " Precision Report ===");
    System.out.println("Total queries analyzed: " + report.getTotalQueryCount());
    System.out.println("Total issues found: " + allIssues.size());
    System.out.println("  Confirmed (ERROR/WARNING): " + report.getConfirmedIssues().size());
    System.out.println("  Info: " + report.getInfoIssues().size());
    System.out.println("True positives: " + truePositives);
    System.out.println("False positives: " + falsePositives);
    System.out.printf("Precision: %.1f%%%n", precision * 100);
    if (!fpDetails.isEmpty()) {
      System.out.println("False positive details:");
      fpDetails.forEach(System.out::println);
    }
    System.out.println("=".repeat(50));

    return new PrecisionResult(truePositives, falsePositives, precision, allIssues.size());
  }

  record PrecisionResult(
      int truePositives, int falsePositives, double precision, int totalIssues) {}

